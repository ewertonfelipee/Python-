num1 = int(input('enter with number 1: '))
num2 = int(input('enter with number 2: '))

oparator = input('enter with oparator + or - or * or /: ')


if(oparator == '+'):
    print(num1 + num2)

elif(oparator == '-'):
    print(num1 - num2)

elif(oparator == '*'):
    print(num1 * num2)

elif(oparator == '/'):
    print(num1 / num2)